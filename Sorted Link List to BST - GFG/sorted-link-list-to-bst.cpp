class Solution{
    TNode *bBst(vector<int> &v,int s,int e){
        TNode *node = NULL;
        if(e >= s){
            int m = s + (e - s + 1)/2;
            node = new TNode(v[m]);
            
            node->left = bBst(v,s,m - 1);
            node->right = bBst(v,m + 1,e);
        }
        
        return node;
    }
  public:
    TNode* sortedListToBST(LNode *head) {
        vector<int> v;
        while(head){
            v.push_back(head->data);
            head = head->next;
        }
        
        return bBst(v,0,v.size() - 1);
    }
};

